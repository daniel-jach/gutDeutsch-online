function showAnswers() {
  var answers = document.querySelectorAll('.answers');
    for (var i = 0; i < answers.length; i++) {
        answers[i].style.display = 'block';
    }
}

function showAnswersTable() {
  var answers = document.querySelectorAll('.answersTable');
    for (var i = 0; i < answers.length; i++) {
        answers[i].style.display = 'block';
    }
}